
public class Punto {

    private Integer x;
    private Integer y;

    public Punto(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public void setX(Integer x) {
        x = x;
    }

    public void setY(Integer y) {
        y = y;
    }

    public  Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

}
